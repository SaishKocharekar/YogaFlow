export const getBMICategory = (bmi) => {
  if (bmi < 18.5) return { category: 'Underweight', key: 'underweight', color: '#3b82f6', advice: 'You should focus on healthy weight gain through nutrient-dense foods and strength-building exercises.' };
  if (bmi < 25) return { category: 'Normal', key: 'normal', color: '#22c55e', advice: 'Great job! Maintain your healthy weight with a balanced diet and regular exercise.' };
  if (bmi < 30) return { category: 'Overweight', key: 'overweight', color: '#f59e0b', advice: 'Consider moderate exercise and a balanced diet to gradually reach a healthier weight.' };
  return { category: 'Obese', key: 'obese', color: '#ef4444', advice: 'Please consult a healthcare provider. Focus on gentle exercises and a medically supervised diet plan.' };
};

export const calculateBMI = (weight, heightCm) => {
  const heightM = heightCm / 100;
  return (weight / (heightM * heightM)).toFixed(1);
};
